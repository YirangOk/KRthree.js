//  KRthree.js 예제 — 랜덤 색 책장
//  책 8권을 원형으로 두고 천천히 돌립니다.
//  미리보기를 마우스로 드래그하면 책장 주위를 돌아다닐 수 있어요.
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

// 장면·카메라·렌더러를 한 번에 만들기
const { 장면, 카메라, 렌더러 } = 삼차원시작({ 배경: '#1a1a1a' })

// 빛 (없으면 3D가 까맣게 보입니다)
const 거리 = 9
빛추가하기(장면, '주변빛', { 색: '흰색', 세기: 0.9 })
빛추가하기(장면, '방향빛', { 색: '흰색', 세기: 0.8, x: 5, y: 5, z: 5 })
빛추가하기(장면, '방향빛', { 색: '흰색', 세기: 0.5, x: -5, y: 5, z: -5 })

// 책 8권을 원형으로 — 각자 랜덤한 색
const 색팔레트 = ['#e74c3c', '#e67e22', '#f1c40f', '#2ecc71', '#1abc9c', '#3498db', '#9b59b6', '#e84393', '#fd79a8', '#6c5ce7']
const 책수 = 8
const 그룹 = 그룹만들기()
const 반지름 = 6
for (let i = 0; i < 책수; i++) {
  const 각도 = (i / 책수) * Math.PI * 2          // 360°를 책 수로 나눈 각도
  const x = Math.cos(각도) * 반지름               // 원 위 가로 자리
  const z = Math.sin(각도) * 반지름               // 원 위 깊이 자리
  const 색 = 색팔레트[Math.floor(Math.random() * 색팔레트.length)]   // 랜덤 색 고르기
  const 책 = 책만들기({ 색: 색, 폭: 2, 높이: 2.8, 두께: 0.3 })       // 표지 없이 색만
  위치설정(책, x, 0, z)
  바라보기(책, x * 2, 0, z * 2)                   // 표지(앞면)가 원 바깥을 향하도록
  그룹에추가하기(그룹, 책)
}
장면에추가하기(장면, 그룹)

// 마우스 드래그로 카메라 돌리기 (책장 주위를 돌아다니기)
let 가로각 = 0.9, 세로각 = 0.5
let 목표가로 = 가로각, 목표세로 = 세로각
let 드래그중 = false, 이전X = 0, 이전Y = 0
window.addEventListener('mousedown', (e) => { 드래그중 = true; 이전X = e.clientX; 이전Y = e.clientY })
window.addEventListener('mouseup', () => { 드래그중 = false })
window.addEventListener('mousemove', (e) => {
  if (!드래그중) return
  목표가로 = 목표가로 + (e.clientX - 이전X) * 0.005
  목표세로 = 목표세로 + (e.clientY - 이전Y) * 0.005
  목표세로 = Math.max(0.15, Math.min(1.3, 목표세로))   // 너무 위/아래로는 안 가게 제한
  이전X = e.clientX; 이전Y = e.clientY
})

// 매 프레임: 책 자동 회전 + 카메라 부드럽게 이동
const 시계 = 시계만들기()
애니메이션시작(() => {
  const dt = 경과시간(시계)
  회전하기(그룹, 0, 0.15 * dt)                    // 책장 천천히 회전
  가로각 = 가로각 + (목표가로 - 가로각) * 0.1      // 목표 각도로 부드럽게 따라가기
  세로각 = 세로각 + (목표세로 - 세로각) * 0.1
  위치설정(카메라,
    거리 * Math.cos(세로각) * Math.cos(가로각),
    거리 * Math.sin(세로각),
    거리 * Math.cos(세로각) * Math.sin(가로각))
  바라보기(카메라, 0, 0, 0)
  렌더링하기(렌더러, 장면, 카메라)
})
